    public void fillDataPres() {
        EleicaoDAO e = new EleicaoDAO();
        ArrayList<String> aux = new ArrayList<String>();
        DefaultListModel<String> dlm = new DefaultListModel<>();
        aux = e.getPresidenciais();
        for (String s : aux) {
            dlm.addElement(s);
        }
        jList2.setModel(dlm);
    }

